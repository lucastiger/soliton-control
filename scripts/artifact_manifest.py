#!/usr/bin/env python
"""One-page manifest of analysis/results/ + hard-checked cross-artifact assertions.

Run from the repo root::

    python scripts/artifact_manifest.py

The manifest is the audit record for the artifact-consistency pass (task A):
every file under ``analysis/results/`` is listed with the script that GENERATES
it and whether THIS consistency pass touched it (regenerated it, corrected it,
or only verified it).  The ``touched`` column is derived from ``git status`` so
it reflects the real working-tree footprint, not a hand-maintained list -- a
file the pass regenerated byte-for-byte identically shows as ``regen (identical)``
(git sees no change), while a genuine content change shows as ``modified``.

The CHECKED SECTION (task B) is a set of cross-artifact consistency assertions
that HARD-FAIL (nonzero exit) on violation, so the manifest doubles as a
regression gate: the single-DKS existence edge, the stationary/breather bands
and the staircase matched-steps must stay mutually consistent across
``detuning_sweep.npz``, ``dks_existence_map.csv`` / ``dks_breathing_scan.csv``
and ``spectral_metrics.json``.

Nothing here re-runs the solver or regenerates an artifact; it is pure
post-processing of the committed files.
"""

